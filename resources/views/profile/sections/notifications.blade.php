<h2 class="section-title">Постановка гипотезы</h2>
<p class="text-muted mb-4">
  Следуйте шагам, чтобы сформулировать и запустить исследовательскую гипотезу.
</p>

<ol class="list-group list-group-numbered mb-4">
  <li class="list-group-item">
    <strong>Сформулируйте гипотезу.</strong>  
    Опишите, что именно вы хотите проверить: анализ ниши, динамику роста, тренд, выручку определённого сегмента или сложную выборку по нескольким факторам.
  </li>
  <li class="list-group-item">
    <strong>Выберите МСС-код.</strong>  
    Определите категорию деятельности из предложенного списка, чтобы уточнить выборку.
  </li>
  <li class="list-group-item">
    <strong>Оплатите гипотезу.</strong>  
    Стоимость формируется исходя из ширины географии, временного диапазона и сложности анализа.
  </li>
  <li class="list-group-item">
    <strong>Ожидайте отчёт.</strong>  
    Аналитика формируется автоматически — обычно в течение одного часа.
  </li>
</ol>
